// Helper function to find a research link by partial match
export function findResearchLink(text: string): string | null {
  // Exact match first
  if (researchLinks[text]) {
    return researchLinks[text];
  }
  
  // Try to find partial match
  const lowerText = text.toLowerCase();
  for (const [key, url] of Object.entries(researchLinks)) {
    if (lowerText.includes(key.toLowerCase()) || key.toLowerCase().includes(lowerText)) {
      return url;
    }
  }
  
  return null;
}
